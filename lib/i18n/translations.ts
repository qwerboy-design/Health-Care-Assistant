const zhTWFlat = flatten(zhTW as Record<string, unknown>);
const enFlat = flatten(en as Record<string, unknown>);

const maps: Record<Locale, Record<string, string>> = {
  'zh-TW': zhTWFlat,
  en: enFlat,
};

/**
 * Returns a translator function for the given locale.
 * Key uses dot path, e.g. 'login.title', 'nav.appName'.
 * Supports simple placeholder like {max} -> pass as second arg object.
 */
export function getT(locale: Locale): (key: string, params?: Record<string, string | number>) => string {
  const dict = maps[locale] ?? maps[DEFAULT_LOCALE];
  return (key: string, params?: Record<string, string | number>) => {
    let value = dict[key] ?? maps[DEFAULT_LOCALE][key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        value = value.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
      }
    }
    return value;
  };
}

